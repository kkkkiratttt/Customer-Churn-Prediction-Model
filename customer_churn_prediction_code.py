# ==========================================
# CUSTOMER CHURN PREDICTION USING ML
# ==========================================

# ------------------------------------------
# STEP 1: Upload Dataset
# ------------------------------------------

# Import file upload utility from Google Colab
from google.colab import files

# Upload CSV dataset from local computer
uploaded = files.upload()

# ------------------------------------------
# STEP 2: Import Pandas Library
# ------------------------------------------

# Pandas is used for data handling and analysis
import pandas as pd

# Read CSV file into DataFrame
df = pd.read_csv("/content/Customer.csv")

# Display first 5 rows of dataset
df.head()

# ------------------------------------------
# STEP 3: Understand the Dataset
# ------------------------------------------

# Show number of rows and columns
df.shape

# Display column names, data types and null values
df.info()

# Generate statistical summary of numerical columns
df.describe()

# ------------------------------------------
# STEP 4: Data Cleaning
# ------------------------------------------

# Remove customerID column because
# it does not help in prediction
df = df.drop("customerID", axis=1)

# Display updated columns
df.columns

# Convert TotalCharges column into numeric datatype
# errors='coerce' converts invalid values into NaN
df["TotalCharges"] = pd.to_numeric(
    df["TotalCharges"],
    errors="coerce"
)

# Check updated datatypes
df.info()

# Remove rows containing missing values
df = df.dropna()

# Verify cleaned dataset
df.info()

# ------------------------------------------
# STEP 5: Separate Features and Target
# ------------------------------------------

# X contains input features
x = df.drop("Churn", axis=1)

# y contains target/output variable
y = df["Churn"]

# Display shape of feature matrix and target vector
print(x.shape)
print(y.shape)

# ------------------------------------------
# STEP 6: Convert Categorical Data
# ------------------------------------------

# Machine learning models cannot understand text
# So convert categorical columns into numerical values

# get_dummies() performs One-Hot Encoding
# drop_first=True avoids dummy variable trap
x = pd.get_dummies(x, drop_first=True)

# Check transformed dataset
x.info()

# Display first 5 rows
x.head()

# ------------------------------------------
# STEP 7: Split Dataset into Training & Testing
# ------------------------------------------

# Import train-test split function
from sklearn.model_selection import train_test_split

# Split data:
# 80% training
# 20% testing
x_train, x_test, y_train, y_test = train_test_split(
    x,
    y,
    test_size=0.2,
    random_state=42
)

# Display dataset sizes
print(x_train.shape)
print(x_test.shape)
print(y_train.shape)
print(y_test.shape)

# ------------------------------------------
# STEP 8: Train Logistic Regression Model
# ------------------------------------------

# Import Logistic Regression algorithm
from sklearn.linear_model import LogisticRegression

# Create model object
model = LogisticRegression(max_iter=1000)

# Train model using training data
model.fit(x_train, y_train)

# ------------------------------------------
# STEP 9: Make Predictions
# ------------------------------------------

# Predict churn values on test dataset
y_pred = model.predict(x_test)

# ------------------------------------------
# STEP 10: Calculate Accuracy
# ------------------------------------------

# Import evaluation metric
from sklearn.metrics import accuracy_score

# Calculate prediction accuracy
accuracy = accuracy_score(y_test, y_pred)

# Print accuracy score
print("Accuracy:", accuracy)

# ------------------------------------------
# STEP 11: Confusion Matrix & Report
# ------------------------------------------

# Import performance metrics
from sklearn.metrics import (
    confusion_matrix,
    classification_report
)

# Generate confusion matrix
cm = confusion_matrix(y_test, y_pred)

# Print confusion matrix
print(cm)

# Print detailed classification report
print("\nClassification Report:\n")
print(classification_report(y_test, y_pred))

# ------------------------------------------
# STEP 12: Improved Logistic Regression
# ------------------------------------------

# Handle class imbalance using class_weight='balanced'
model = LogisticRegression(
    class_weight='balanced',
    max_iter=1000
)

# Train improved model
model.fit(x_train, y_train)

# Predict again
y_pred = model.predict(x_test)

# Print new accuracy
print(
    "Accuracy:",
    accuracy_score(y_test, y_pred)
)

